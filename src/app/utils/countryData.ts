// Helper function to get country by code
export const getCountryByCode = (code: string): CountryData | undefined => {
  return COMPLETE_COUNTRY_DATA.find((country) => country.code === code);
};

// Helper function to get country by phone code
export const getCountryByPhoneCode = (
  phoneCode: string
): CountryData | undefined => {
  return COMPLETE_COUNTRY_DATA.find(
    (country) => country.phoneCode === phoneCode
  );
};

// Helper function to get all unique phone codes
export const getAllPhoneCodes = (): string[] => {
  return Array.from(
    new Set(COMPLETE_COUNTRY_DATA.map((country) => country.phoneCode))
  ).sort();
};

